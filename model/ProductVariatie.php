/**
 * Description of ProductVariatie
 *
 * @author Myra
 */
class ProductVariatie {
    private $productVariatieId;
    private $productId;
    private $kleurId;
    
    function __construct($productVariatieId, $productId, $kleurId) {
        $this->productVariatieId = $productVariatieId;
        $this->productId = $productId;
        $this->kleurId = $kleurId;
    }
    function getProductVariatieId() {
        return $this->productVariatieId;
    }

    function getProductId() {
        return $this->productId;
    }

    function getKleurId() {
        return $this->kleurId;
    }

    function setProductVariatieId($productVariatieId) {
        $this->productVariatieId = $productVariatieId;
    }

    function setProductId($productId) {
        $this->productId = $productId;
    }

    function setKleurId($kleurId) {
        $this->kleurId = $kleurId;
    }




}
