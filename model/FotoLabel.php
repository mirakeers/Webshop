/**
 * Description of FotoLabel
 *
 * @author Myra
 */
class FotoLabel {
    private $fotoLabelId;
    private $label;
    function __construct($fotoLabelId, $label) {
        $this->fotoLabelId = $fotoLabelId;
        $this->label = $label;
    }
    function getFotoLabelId() {
        return $this->fotoLabelId;
    }

    function getLabel() {
        return $this->label;
    }

    function setFotoLabelId($fotoLabelId) {
        $this->fotoLabelId = $fotoLabelId;
    }

    function setLabel($label) {
        $this->label = $label;
    }
}
