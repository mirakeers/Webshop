/**
 * Description of Foto
 *
 * @author Myra
 */
class Foto {
    private $fotoId;
    private $locatieFull;
    private $locatieThumb;
    private $productVariatieId;
    private $fotoLabelId;
    private $prioriteit;
    
    
    function __construct($fotoId, $locatieFull, $locatieThumb, $productVariatieId, $fotoLabelId, $prioriteit) {
        $this->fotoId = $fotoId;
        $this->locatieFull = $locatieFull;
        $this->locatieThumb = $locatieThumb;
        $this->productVariatieId = $productVariatieId;
        $this->fotoLabelId = $fotoLabelId;
        $this->prioriteit = $prioriteit;
    }
    function getFotoId() {
        return $this->fotoId;
    }

    function getLocatieFull() {
        return $this->locatieFull;
    }

    function getLocatieThumb() {
        return $this->locatieThumb;
    }

    function getProductVariatieId() {
        return $this->productVariatieId;
    }

    function getFotoLabelId() {
        return $this->fotoLabelId;
    }

    function getPrioriteit() {
        return $this->prioriteit;
    }

    function setFotoId($fotoId) {
        $this->fotoId = $fotoId;
    }

    function setLocatieFull($locatieFull) {
        $this->locatieFull = $locatieFull;
    }

    function setLocatieThumb($locatieThumb) {
        $this->locatieThumb = $locatieThumb;
    }

    function setProductVariatieId($productVariatieId) {
        $this->productVariatieId = $productVariatieId;
    }

    function setFotoLabelId($fotoLabelId) {
        $this->fotoLabelId = $fotoLabelId;
    }

    function setPrioriteit($prioriteit) {
        $this->prioriteit = $prioriteit;
    }
}
